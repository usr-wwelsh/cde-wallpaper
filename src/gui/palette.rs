pub fn get_palette_colors(name: &str) -> &'static [[u8; 3]] {
    CDE_PALETTES
        .iter()
        .find(|(n, _)| *n == name)
        .map(|(_, c)| *c)
        .unwrap_or(CDE_PALETTES[12].1) // Default
}

/// Clear and repopulate a FlowBox with color swatches.
pub fn populate_flow(
    flow: &FlowBox,
    colors: &[[u8; 3]],
    state: Rc<RefCell<AppState>>,
    set_fg: bool,
    on_change: Rc<dyn Fn()>,
) {
    while let Some(child) = flow.first_child() {
        flow.remove(&child);
    }

    for &color in colors {
        let da = DrawingArea::builder()
            .content_width(28)
            .content_height(28)
            .build();

        da.set_draw_func(move |_, cr, _, _| {
            cr.set_source_rgb(
                color[0] as f64 / 255.0,
                color[1] as f64 / 255.0,
                color[2] as f64 / 255.0,
            );
            let _ = cr.paint();
        });

        let gesture = GestureClick::new();
        let state_c = Rc::clone(&state);
        let on_change_c = Rc::clone(&on_change);
        gesture.connect_pressed(move |_, _, _, _| {
            {
                let mut s = state_c.borrow_mut();
                if set_fg { s.config.fg_color = color; } else { s.config.bg_color = color; }
            }
            on_change_c();
        });
        da.add_controller(gesture);
        flow.append(&da);
    }
}

pub fn build_palette(
    colors: &[[u8; 3]],
    state: Rc<RefCell<AppState>>,
    set_fg: bool,
    on_change: Rc<dyn Fn()>,
) -> FlowBox {
    let flow = FlowBox::builder()
        .selection_mode(SelectionMode::None)
        .max_children_per_line(8)
        .min_children_per_line(8)
        .row_spacing(2)
        .column_spacing(2)
        .build();

    populate_flow(&flow, colors, state, set_fg, on_change);
    flow
}
